const RECOMMENDATION_TAGS = ['strong_buy', 'buy', 'hold', 'sell', 'strong_sell'] as const;

/** Tags that are set automatically; users cannot add them manually when adding to watchlist. */
const AUTO_GENERATED_TAGS = new Set<string>([
  'portfolio',
  'dividend',
  ...RECOMMENDATION_TAGS,
]);

function isAutoGeneratedTag(tag: string): boolean {
  return AUTO_GENERATED_TAGS.has(tag.trim().toLowerCase());
}

function formatRecommendation(key: string): string {
  if (!key) return '—';
  return key
    .split('_')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join(' ');
}

function isRecommendationTag(tag: string): tag is (typeof RECOMMENDATION_TAGS)[number] {
  return RECOMMENDATION_TAGS.includes(tag as (typeof RECOMMENDATION_TAGS)[number]);
}

/** Normalize API recommendation value to our tag key (e.g. "Strong Buy" or "strong_buy" -> "strong_buy"). */
function normalizeRecommendationKey(raw: string): (typeof RECOMMENDATION_TAGS)[number] | null {
  const s = raw.trim().toLowerCase().replace(/\s+/g, '_');
  if (RECOMMENDATION_TAGS.includes(s as (typeof RECOMMENDATION_TAGS)[number]))
    return s as (typeof RECOMMENDATION_TAGS)[number];
  const map: Record<string, (typeof RECOMMENDATION_TAGS)[number]> = {
    strongbuy: 'strong_buy',
    strongsell: 'strong_sell',
  };
  return map[s] ?? null;
}
