export function Watchlist() {
  const items = useWatchlist();
  const holdings = useHoldings();
  const allTags = useWatchlistTags();
  const marketState = useMarketState();
  const latestIVs = useLatestIntrinsicValues();
  const [summaries, setSummaries] = useState<Map<string, TickerSummary>>(
    new Map()
  );
  const [loading, setLoading] = useState(false);
  const [activeTag, setActiveTag] = useState<string | null>(null);
  const [showAdd, setShowAdd] = useState(false);
  const [chartTicker, setChartTicker] = useState<string | null>(null);
  const [sortCol, setSortCol] = useState<'ticker' | 'price' | 'change' | 'intrinsic' | 'target' | 'min' | 'dividend'>('min');
  const [sortDir, setSortDir] = useState<'asc' | 'desc'>('desc');

  // Re-run portfolio sync whenever holdings change so "portfolio" tag stays correct
  const holdingsKey = useMemo(
    () => holdings.map((h) => h.ticker).join(','),
    [holdings]
  );
  useEffect(() => {
    syncPortfolioToWatchlist();
  }, [holdingsKey]);

  const tickerKey = items.map((w) => w.ticker).join(',');

  const fetchAll = useCallback(async () => {
    const tickers = tickerKey ? tickerKey.split(',') : [];
    if (tickers.length === 0) {
      setSummaries(new Map());
      return;
    }
    setLoading(true);
    try {
      await clearAllCaches();
      setSummaries(await fetchTickerSummaries(tickers));
    } finally {
      setLoading(false);
    }
  }, [tickerKey]);

  useEffect(() => {
    const tickers = tickerKey ? tickerKey.split(',') : [];
    if (tickers.length === 0) return;
    setLoading(true);
    fetchTickerSummaries(tickers)
      .then(setSummaries)
      .finally(() => setLoading(false));
  }, [tickerKey]);

  useRegisterRefresh('watchlist-summaries', fetchAll);

  // Sync "dividend" tag on watchlist items when summary data is available (re-checked on each load/refresh)
  useEffect(() => {
    const dividendTag = 'dividend';
    for (const item of items) {
      if (item.id == null) continue;
      const s = summaries.get(item.ticker);
      const hasDividend = !!(s && (s.dividendYield > 0 || s.dividendRate > 0));
      const hasTag = item.tags.includes(dividendTag);
      if (hasDividend && !hasTag) {
        updateWatchlistItem(item.id, { tags: [...item.tags, dividendTag] });
      } else if (!hasDividend && hasTag) {
        updateWatchlistItem(item.id, {
          tags: item.tags.filter((t) => t !== dividendTag),
        });
      }
    }
  }, [items, summaries]);

  // Sync recommendation tag on watchlist items when summary data is available (re-checked on each load/refresh)
  useEffect(() => {
    for (const item of items) {
      if (item.id == null) continue;
      const s = summaries.get(item.ticker);
      const wantedTag = s?.recommendation ? normalizeRecommendationKey(s.recommendation) : null;
      const hasRecTag = RECOMMENDATION_TAGS.some((t) => item.tags.includes(t));
      if (wantedTag) {
        const otherRecTags = item.tags.filter((t) => isRecommendationTag(t));
        if (!otherRecTags.includes(wantedTag) || otherRecTags.length > 1) {
          const withoutRec = item.tags.filter((t) => !isRecommendationTag(t));
          updateWatchlistItem(item.id, { tags: [...withoutRec, wantedTag] });
        }
      } else if (hasRecTag) {
        updateWatchlistItem(item.id, {
          tags: item.tags.filter((t) => !isRecommendationTag(t)),
        });
      }
    }
  }, [items, summaries]);

  const filtered = useMemo(
    () =>
      activeTag ? items.filter((w) => w.tags.includes(activeTag)) : items,
    [items, activeTag]
  );

  // Tag filter order: portfolio, dividend, then recommendations (strong_buy → strong_sell), then any other tags
  const sortedTags = useMemo(() => {
    const set = new Set(allTags);
    const result: string[] = [];
    if (set.has('portfolio')) {
      result.push('portfolio');
      set.delete('portfolio');
    }
    if (set.has('dividend')) {
      result.push('dividend');
      set.delete('dividend');
    }
    for (const t of RECOMMENDATION_TAGS) {
      if (set.has(t)) {
        result.push(t);
        set.delete(t);
      }
    }
    result.push(...set);
    return result;
  }, [allTags]);

  const sorted = useMemo(() => {
    const getVal = (item: typeof filtered[0]) => {
      const s = summaries.get(item.ticker);
      const iv = latestIVs.get(item.ticker)?.value;
      const target = s?.targetMedian ?? 0;
      const price = s?.price ?? 0;
      const changePct = price > 0 ? (s?.changePercent ?? 0) : 0;
      const ivUpside = iv && price > 0 ? ((iv - price) / price) * 100 : -Infinity;
      const targetUpside = target > 0 && price > 0 ? ((target - price) / price) * 100 : -Infinity;
      const candidates = [iv, target].filter((v): v is number => v != null && v > 0);
      const minVal = candidates.length > 0 ? Math.min(...candidates) : 0;
      const minUpside = minVal > 0 && price > 0 ? ((minVal - price) / price) * 100 : -Infinity;

      switch (sortCol) {
        case 'ticker': return 0;
        case 'price': return price;
        case 'change': return changePct;
        case 'intrinsic': return ivUpside;
        case 'target': return targetUpside;
        case 'min': return minUpside;
        case 'dividend': return s?.dividendYield ?? -Infinity;
      }
    };

    const arr = [...filtered];
    if (sortCol === 'ticker') {
      arr.sort((a, b) => sortDir === 'asc'
        ? a.ticker.localeCompare(b.ticker)
        : b.ticker.localeCompare(a.ticker));
    } else {
      arr.sort((a, b) => {
        const va = getVal(a);
        const vb = getVal(b);
        return sortDir === 'asc' ? va - vb : vb - va;
      });
    }
    return arr;
  }, [filtered, summaries, latestIVs, sortCol, sortDir]);

  function toggleSort(col: typeof sortCol) {
    if (sortCol === col) {
      setSortDir((d) => (d === 'asc' ? 'desc' : 'asc'));
    } else {
      setSortCol(col);
      setSortDir(col === 'ticker' ? 'asc' : 'desc');
    }
  }

  return (
    <div>
      <div className="flex items-center justify-between mb-6">
        <div className="flex items-center gap-3">
          <h1 className="text-2xl font-bold text-gray-900 dark:text-white">
            Watchlist
          </h1>
          <MarketBadge state={marketState} />
        </div>
        <div className="flex items-center gap-2">
          <RefreshButton />
          <button
            onClick={() => setShowAdd(true)}
            className="flex items-center gap-1.5 px-3 py-1.5 text-xs font-medium rounded-lg bg-indigo-600 text-white hover:bg-indigo-700 transition-colors"
          >
            <Plus size={14} />
            Add Ticker
          </button>
        </div>
      </div>

      {sortedTags.length > 0 && (
        <div className="flex flex-wrap gap-1.5 mb-5">
          <button
            onClick={() => setActiveTag(null)}
            className={cn(
              'px-2.5 py-1 text-xs font-medium rounded-full transition-colors',
              activeTag === null
                ? 'bg-indigo-600 text-white'
                : 'bg-gray-100 dark:bg-slate-800 text-gray-600 dark:text-slate-400 hover:bg-gray-200 dark:hover:bg-slate-700'
            )}
          >
            All
          </button>
          {sortedTags.map((tag) => {
            const isRec = isRecommendationTag(tag);
            const label = isRec ? formatRecommendation(tag) : tag;
            return (
              <button
                key={tag}
                onClick={() => setActiveTag(tag === activeTag ? null : tag)}
                className={cn(
                  'px-2.5 py-1 text-xs font-medium rounded-full transition-colors',
                  tag === activeTag
                    ? 'bg-indigo-600 text-white'
                    : tag === 'portfolio'
                      ? 'bg-emerald-100 dark:bg-emerald-900/30 text-emerald-700 dark:text-emerald-400 hover:bg-emerald-200 dark:hover:bg-emerald-900/50'
                      : tag === 'dividend'
                        ? 'bg-amber-100 dark:bg-amber-900/30 text-amber-700 dark:text-amber-400 hover:bg-amber-200 dark:hover:bg-amber-900/50'
                        : isRec
                          ? tag === 'strong_buy' || tag === 'buy'
                            ? 'bg-emerald-100 dark:bg-emerald-900/30 text-emerald-700 dark:text-emerald-400 hover:bg-emerald-200 dark:hover:bg-emerald-900/50'
                            : tag === 'strong_sell' || tag === 'sell'
                              ? 'bg-red-100 dark:bg-red-900/30 text-red-700 dark:text-red-400 hover:bg-red-200 dark:hover:bg-red-900/50'
                              : 'bg-slate-100 dark:bg-slate-800 text-slate-600 dark:text-slate-400 hover:bg-slate-200 dark:hover:bg-slate-700'
                          : 'bg-gray-100 dark:bg-slate-800 text-gray-600 dark:text-slate-400 hover:bg-gray-200 dark:hover:bg-slate-700'
                )}
              >
                {label}
              </button>
            );
          })}
        </div>
      )}

      {showAdd && (
        <AddTickerForm
          onDone={() => setShowAdd(false)}
          existingTickers={items.map((w) => w.ticker)}
        />
      )}

      {sorted.length === 0 ? (
        <EmptyState
          icon={<Eye size={48} />}
          title="Watchlist is empty"
          description={
            activeTag
              ? `No items tagged "${activeTag}".`
              : 'Add tickers to start watching.'
          }
        />
      ) : (
        <div className="bg-white dark:bg-slate-900 rounded-xl border border-gray-200 dark:border-slate-800 overflow-hidden">
          <div className="grid grid-cols-[1fr_90px_90px_90px_90px_90px_70px_70px_130px_70px] gap-2 px-4 py-2.5 text-xs font-medium text-gray-500 dark:text-slate-400 border-b border-gray-100 dark:border-slate-800">
            <SortHeader label="Ticker" col="ticker" current={sortCol} dir={sortDir} onSort={toggleSort} />
            <SortHeader label="Price" col="price" current={sortCol} dir={sortDir} onSort={toggleSort} align="right" />
            <SortHeader label="Change" col="change" current={sortCol} dir={sortDir} onSort={toggleSort} align="right" />
            <SortHeader label="Intrinsic" col="intrinsic" current={sortCol} dir={sortDir} onSort={toggleSort} align="right" />
            <SortHeader label="Target" col="target" current={sortCol} dir={sortDir} onSort={toggleSort} align="right" />
            <SortHeader label="Min(IV, T)" col="min" current={sortCol} dir={sortDir} onSort={toggleSort} align="right" />
            <SortHeader label="Div" col="dividend" current={sortCol} dir={sortDir} onSort={toggleSort} align="right" />
            <span className="text-right">Rec</span>
            <span className="text-center">52w Range</span>
            <span />
          </div>

          {sorted.map((item) => (
            <WatchlistRow
              key={item.id}
              item={item}
              summary={summaries.get(item.ticker)}
              latestIV={latestIVs.get(item.ticker)}
              loading={loading}
              onRowClick={() => setChartTicker(item.ticker)}
            />
          ))}
        </div>
      )}

      {chartTicker && (
        <TickerDetailModal
          ticker={chartTicker}
          summary={summaries.get(chartTicker)}
          onClose={() => setChartTicker(null)}
        />
      )}
    </div>
  );
}

// --------------- Sort Header ---------------

type SortCol = 'ticker' | 'price' | 'change' | 'intrinsic' | 'target' | 'min' | 'dividend';

function SortHeader({
  label,
  col,
  current,
  dir,
  onSort,
  align,
}: {
  label: string;
  col: SortCol;
  current: SortCol;
  dir: 'asc' | 'desc';
  onSort: (col: SortCol) => void;
  align?: 'right';
}) {
  const active = current === col;
  return (
    <button
      onClick={() => onSort(col)}
      className={cn(
        'flex items-center gap-0.5 transition-colors hover:text-gray-700 dark:hover:text-slate-200',
        align === 'right' && 'justify-end',
        active && 'text-indigo-600 dark:text-indigo-400'
      )}
    >
      {label}
      {active && (dir === 'asc' ? <ArrowUp size={10} /> : <ArrowDown size={10} />)}
    </button>
  );
}

